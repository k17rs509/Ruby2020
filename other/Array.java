class Array {
    public static void main(String[] args){
        int[] ia = {10, 20, 30, 40};

        int i=0;
        while (i<ia.length) {
            System.out.print(ia[i] + ",");
            i++;
        }
        System.out.println();

        String[] sa = {"January", "February", "March"};
        i=0;
        while (i<sa.length) {
            System.out.print(sa[i] + ",");
            i++;
        }
        System.out.println();

    }
}